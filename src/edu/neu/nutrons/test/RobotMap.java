package edu.neu.nutrons.test;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {

    public static final int PAD = 1;
    public static final int L_DRIVE_MOTOR = 2;
    public static final int R_DRIVE_MOTOR = 3;
    public static final int CAM_SERVO = 4;
    public static final int L_DRIVE_ENC_A = 3;
    public static final int L_DRIVE_ENC_B = 4;
    public static final int R_DRIVE_ENC_A = 1;
    public static final int R_DRIVE_ENC_B = 2;
}
